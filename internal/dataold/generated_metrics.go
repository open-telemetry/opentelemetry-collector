func (ms SummaryValueAtPercentile) CopyTo(dest SummaryValueAtPercentile) {
	if ms.IsNil() {
		*dest.orig = nil
		return
	}
	if dest.IsNil() {
		dest.InitEmpty()
	}
	dest.SetPercentile(ms.Percentile())
	dest.SetValue(ms.Value())
}
