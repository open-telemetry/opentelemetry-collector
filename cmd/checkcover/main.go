package main

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

func main() {
	rootCmd := &cobra.Command{
		Use:   "checkcover",
		Short: "Validate code coverage requirements for OpenTelemetry Collector components",
		Long: `checkcover validates that components meet minimum code coverage requirements
based on their stability level and any custom coverage_minimum settings in metadata.yaml.

Stable components must have at least 80% coverage or the repository minimum, whichever is higher.
Components can specify a higher requirement via the coverage_minimum field in metadata.yaml.`,
		RunE: run,
	}

	rootCmd.Flags().StringP("coverage-file", "c", "coverage.txt", "Path to the coverage file (generated by 'go tool covdata textfmt')")
	rootCmd.Flags().StringP("repo-root", "r", ".", "Root directory of the repository")
	rootCmd.Flags().IntP("repo-minimum", "m", 0, "Repository-wide minimum coverage percentage")
	rootCmd.Flags().BoolP("verbose", "v", false, "Enable verbose output")

	if err := rootCmd.Execute(); err != nil {
		fmt.Fprintf(os.Stderr, "Error: %v\n", err)
		os.Exit(1)
	}
}

func run(cmd *cobra.Command, args []string) error {
	coverageFile, _ := cmd.Flags().GetString("coverage-file")
	repoRoot, _ := cmd.Flags().GetString("repo-root")
	repoMinimum, _ := cmd.Flags().GetInt("repo-minimum")
	verbose, _ := cmd.Flags().GetBool("verbose")

	if verbose {
		fmt.Printf("Coverage file: %s\n", coverageFile)
		fmt.Printf("Repository root: %s\n", repoRoot)
		fmt.Printf("Repository minimum: %d%%\n", repoMinimum)
	}

	validator := NewValidator(repoRoot, repoMinimum, verbose)
	
	if err := validator.LoadCoverage(coverageFile); err != nil {
		return fmt.Errorf("failed to load coverage data: %w", err)
	}

	if err := validator.ValidateComponents(); err != nil {
		return fmt.Errorf("coverage validation failed: %w", err)
	}

	fmt.Println("âœ… All components meet coverage requirements")
	return nil
}
